namespace Magenest\MegaMenu\Controller\Adminhtml;

abstract class Label extends \Magento\Backend\App\Action
{
    /**
     * Init actions
     *
     * @return $this
     */
    protected function _initAction()
    {
        // load layout, set active menu and breadcrumbs
        $this->_view->loadLayout();
        $this->_setActiveMenu(
            'Magenest_MegaMenu::label'
        )->_addBreadcrumb(
            __('Buttons'),
            __('Buttons')
        );

        return $this;
    }

    /**
     * Check the permission to run it
     *
     * @return bool
     */
    protected function _isAllowed()
    {
        return $this->_authorization->isAllowed('Magenest_MegaMenu::label');
    }
}
